package com.techment.codeTest;

public class TrafficLight {

	public static void main(String[] args) {
		  
		
		String color = "red";
			
		switch(color)
		{
			
		case "red":
			System.out.println("stop");
			break;
			
		case "yellow":
			System.out.println("ready");
			break;
				
		case "green":
			System.out.println("go");
			break;
		
		default:	
			
			System.out.println(" ");
			}

	}

}
